

package entregavel;


public class Professor {

    //Atributos

    String nomeProf;
    String sobrenomeProf;
    int tempoDeCasaProf;
    int codigoProf;





    //Getters e Setter

    public int getCodigoProf() {
        return codigoProf;
    }

    public void setCodigoProf(int codigoProf) {
        this.codigoProf = codigoProf;
    }

    public String getNomeProf() {
        return nomeProf;
    }

    public void setNomeProf(String nomeProf) {
        this.nomeProf = nomeProf;
    }

    public String getSobrenomeProf() {
        return sobrenomeProf;
    }

    public void setSobrenomeProf(String sobrenomeProf) {
        this.sobrenomeProf = sobrenomeProf;
    }

    public int getTempoDeCasaProf() {
        return tempoDeCasaProf;
    }

    public void setTempoDeCasaProf(int tempoDeCasaProf) {
        this.tempoDeCasaProf = tempoDeCasaProf;
    }



}
